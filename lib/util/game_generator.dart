  static const subCategories = ['头盔', '项链', '护肩', '长袍', '披风', '护腕', '护腿', '鞋'];
  static Item item({int? category}) {
    int randomCategory = category ?? Random().nextInt(9);
    String description = '随处可见。';
    int id = GameHelper.calculateId();
    String name = '随机';
    int quality = Random().nextInt(6); // 白 绿 蓝 紫 橙 红
    int quantity = randomCategory > 0 ? Random().nextInt(100) : 1;
    int rank = Random().nextInt(4);
    int subcategory = Random().nextInt(8);
    List<int> attributes = [];

    String affix = categories[randomCategory];
    if (randomCategory == 0) {
      affix = subCategories[subcategory];
    }
    name = '$name$affix';

    int count = Random().nextInt(5);
    for (int i = 0; i < count; i++) {
      attributes.add(Random().nextInt(10));
    }

    return Item(
      category: randomCategory,
      description: description,
      id: id,
      name: name,
      quality: quality,
      quantity: quantity,
      rank: rank,
      subcategory: subcategory,
      attributes: attributes,
    );
  }
